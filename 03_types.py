# String type
my_name = "Nicolas"
# It could be initialize either with single quote o double quote
my_name = 'Juan'
print("my_name =>", my_name)

# The type function returns the class type of the argument
print(type(my_name))

# int type
my_age = 25
print("my_age =>", my_age)
print(type(my_age))

# float type
my_height = 1.70
print("my_height =>", my_age)
print(type(my_height))

# boolean type (True OR False)
is_single = True
print("is_single =>", is_single)
print(type(is_single))

